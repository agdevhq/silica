import fs from "node:fs/promises";
import path from "node:path";
import process from "node:process";
import { fileURLToPath } from "node:url";

const repoRoot = new URL("..", import.meta.url);
const outputPath = new URL(
  "../packages/cli/src/scaffold-versions.ts",
  import.meta.url,
);

const scaffoldPackages = [
  ["@silicajs/cli", "packages/cli/package.json"],
  ["@silicajs/core", "packages/core/package.json"],
  ["@silicajs/next", "packages/next/package.json"],
  ["@silicajs/theme-amethyst", "packages/theme-amethyst/package.json"],
];

const mode = process.argv[2];

if (!["--check", "--write"].includes(mode)) {
  console.error(
    "Usage: node scripts/sync-scaffold-versions.mjs --check|--write",
  );
  process.exit(1);
}

const ranges = Object.fromEntries(
  await Promise.all(
    scaffoldPackages.map(async ([packageName, manifestPath]) => {
      const manifestUrl = new URL(manifestPath, repoRoot);
      const manifest = JSON.parse(await fs.readFile(manifestUrl, "utf8"));
      if (manifest.name !== packageName) {
        throw new Error(
          `Expected ${manifestPath} to define ${packageName}, found ${manifest.name}`,
        );
      }
      return [packageName, `^${manifest.version}`];
    }),
  ),
);

const rangeLines = Object.entries(ranges)
  .map(
    ([packageName, range]) =>
      `  ${JSON.stringify(packageName)}: ${JSON.stringify(range)},`,
  )
  .join("\n");

const expected = `// This file is generated by scripts/sync-scaffold-versions.mjs.
// Run npm run scaffold-versions:update after package version changes.

export const scaffoldDependencyRanges = {
${rangeLines}
} as const;
`;

if (mode === "--write") {
  await fs.writeFile(outputPath, expected);
  process.exit(0);
}

const current = await fs.readFile(outputPath, "utf8").catch(() => "");
if (current !== expected) {
  const relativeOutput = path.relative(
    process.cwd(),
    fileURLToPath(outputPath),
  );
  console.error(`${relativeOutput} is out of date.`);
  console.error("Run npm run scaffold-versions:update.");
  process.exit(1);
}
