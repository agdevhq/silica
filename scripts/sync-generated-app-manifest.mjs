import fs from "node:fs/promises";
import path from "node:path";
import process from "node:process";
import { fileURLToPath } from "node:url";

const repoRoot = new URL("..", import.meta.url);
const outputPath = new URL(
  "../packages/next/src/generated-app-package-manifest.ts",
  import.meta.url,
);

const mode = process.argv[2];

if (!["--check", "--write"].includes(mode)) {
  console.error(
    "Usage: node scripts/sync-generated-app-manifest.mjs --check|--write",
  );
  process.exit(1);
}

const nextManifest = await readManifest("packages/next/package.json");
const uiManifest = await readManifest("packages/ui/package.json");
const themeManifest = await readManifest(
  "packages/theme-amethyst/package.json",
);

const dependencies = {
  "@silicajs/next": packageVersionRange(nextManifest),
  "@silicajs/theme-amethyst": packageVersionRange(themeManifest),
  "@silicajs/ui": packageVersionRange(uiManifest),
  "@tailwindcss/postcss": dependencyRange(
    uiManifest,
    "devDependencies",
    "@tailwindcss/postcss",
  ),
  "better-sqlite3": dependencyRange(
    nextManifest,
    "dependencies",
    "better-sqlite3",
  ),
  jiti: dependencyRange(nextManifest, "dependencies", "jiti"),
  next: dependencyRange(nextManifest, "peerDependencies", "next"),
  react: dependencyRange(nextManifest, "peerDependencies", "react"),
  "react-dom": dependencyRange(nextManifest, "peerDependencies", "react-dom"),
  tailwindcss: dependencyRange(uiManifest, "devDependencies", "tailwindcss"),
};

for (const [packageName, range] of Object.entries(nextManifest.dependencies)) {
  if (packageName.startsWith("@silicajs/")) dependencies[packageName] = range;
}

const devDependencies = {
  "@types/better-sqlite3": dependencyRange(
    nextManifest,
    "devDependencies",
    "@types/better-sqlite3",
  ),
  "@types/node": dependencyRange(
    nextManifest,
    "devDependencies",
    "@types/node",
  ),
  typescript: dependencyRange(nextManifest, "devDependencies", "typescript"),
};

const expected = `// This file is generated by scripts/sync-generated-app-manifest.mjs.
// Run npm run generated-app-manifest:update after generated app dependency changes.

export type GeneratedAppPackageManifest = {
  dependencies: Record<string, string>;
  devDependencies: Record<string, string>;
};

export const generatedAppPackageManifest: GeneratedAppPackageManifest = {
  dependencies: {
${objectEntries(sortObject(dependencies))}
  },
  devDependencies: {
${objectEntries(sortObject(devDependencies))}
  },
};
`;

if (mode === "--write") {
  await fs.writeFile(outputPath, expected);
  process.exit(0);
}

const current = await fs.readFile(outputPath, "utf8").catch(() => "");
if (current !== expected) {
  const relativeOutput = path.relative(
    process.cwd(),
    fileURLToPath(outputPath),
  );
  console.error(`${relativeOutput} is out of date.`);
  console.error("Run npm run generated-app-manifest:update.");
  process.exit(1);
}

async function readManifest(relativePath) {
  return JSON.parse(await fs.readFile(new URL(relativePath, repoRoot), "utf8"));
}

function packageVersionRange(manifest) {
  if (!manifest.version) {
    throw new Error(`${manifest.name} is missing a version.`);
  }
  return `^${manifest.version}`;
}

function dependencyRange(manifest, section, packageName) {
  const range = manifest[section]?.[packageName];
  if (!range) {
    throw new Error(`${manifest.name} is missing ${section}.${packageName}.`);
  }
  return range;
}

function sortObject(object) {
  return Object.fromEntries(
    Object.entries(object).sort(([left], [right]) => left.localeCompare(right)),
  );
}

function objectEntries(object) {
  return Object.entries(object)
    .map(([key, value]) => `    ${propertyKey(key)}: ${JSON.stringify(value)},`)
    .join("\n");
}

function propertyKey(key) {
  return /^[$A-Z_a-z][$\w]*$/.test(key) ? key : JSON.stringify(key);
}
